import threading

lock = threading.Lock()


class SingletonThreadSafe(type):
    _instances = {}

    def __call__(cls, *args, **kwargs):
        if cls not in cls._instances:
            with lock:
                if cls not in cls._instances:
                    cls._instances[cls] = super(SingletonThreadSafe, cls).__call__(*args, **kwargs)
        return cls._instances[cls]
